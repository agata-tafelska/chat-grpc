package atafelska.chat.client.core;

public class SceneConfiguration {
    private int width;
    private int height;

    public SceneConfiguration(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
